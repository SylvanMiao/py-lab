#有火柴21根，两人依次取，每人每次只可以取走1~4根，不能多取，也不能不取
#谁取到最后一根火柴就算输，编写一个人机对弈程序，计算机后取但计算机常胜

#只要保证每一轮抽取次数(人抽取一次，计算机抽取一次)时人抽取到的火柴数和计算机抽取到的火柴数加起来总数为5即可

if __name__=="__main__":
    total=21
    while total>1:
        n=int(input("你抽取的火柴数是:"))
        if n<=4 and n>=1:
            t=5-n
            print("计算机抽取的火柴数为: %d" %t)
            total-=5
            print("此时还剩%d根火柴" %total)
        else:
            print("请输入1~4的火柴数")
    print(" ")

    print("you lose")

    print(" ")